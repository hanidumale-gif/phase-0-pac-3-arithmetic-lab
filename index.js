// 1. Adds two numbers and returns the result
function add(a, b) {
  return a + b;
}

// 2. Subtracts b from a and returns the result
function subtract(a, b) {
  return a - b;
}

// 3. Multiplies two numbers and returns the result
function multiply(a, b) {
  return a * b;
}

// 4. Divides a by b and returns the result
function divide(a, b) {
  return a / b;
}

// 5. Increments the provided number by 1 and returns the result
function increment(n) {
  return n + 1;
}

// 6. Decrements the provided number by 1 and returns the result
function decrement(n) {
  return n - 1;
}

// 7. Converts a string to an integer (base 10) and returns it
function makeInt(string) {
  return parseInt(string, 10);
}

// 8. Converts a string to a decimal number (float) and returns it
function preserveDecimal(string) {
  return parseFloat(string);
}
